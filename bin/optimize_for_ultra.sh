#!/usr/bin/env bash
# Optimize integrated-ai-platform for Mac Studio M3 Ultra (28-core CPU, 60-core GPU, 96GB RAM)
# Safe to run on any Mac — will detect hardware and skip inapplicable tuning.
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
REPORT_FILE="${REPO_ROOT}/docs/MAC_STUDIO.md"

# ── Colour helpers ─────────────────────────────────────────────────────────────
G='\033[0;32m'; Y='\033[0;33m'; R='\033[0;31m'; B='\033[0;34m'; N='\033[0m'
info()  { echo -e "${B}[INFO]${N}  $*"; }
ok()    { echo -e "${G}[ OK ]${N}  $*"; }
warn()  { echo -e "${Y}[WARN]${N}  $*"; }
err()   { echo -e "${R}[ERR ]${N}  $*"; }
header(){ echo -e "\n${B}══════════════════════════════════════════${N}"; echo -e "${B} $* ${N}"; echo -e "${B}══════════════════════════════════════════${N}"; }

# ── Detect hardware ────────────────────────────────────────────────────────────
header "Hardware Detection"

CPU_BRAND="$(sysctl -n machdep.cpu.brand_string 2>/dev/null || echo 'unknown')"
CPU_CORES="$(sysctl -n hw.physicalcpu 2>/dev/null || echo 0)"
RAM_GB=$(( $(sysctl -n hw.memsize 2>/dev/null || echo 0) / 1073741824 ))
GPU_CORES="$(system_profiler SPDisplaysDataType 2>/dev/null | grep -i 'total number of cores' | awk '{print $NF}' | head -1 || echo 'unknown')"

info "CPU: ${CPU_BRAND}"
info "Physical cores: ${CPU_CORES}"
info "RAM: ${RAM_GB}GB"
info "GPU cores: ${GPU_CORES}"

IS_ULTRA=false
if [[ "${CPU_BRAND}" == *"M3 Ultra"* ]] || \
   [[ "${CPU_BRAND}" == *"M4 Ultra"* ]] || \
   ( [[ "${CPU_CORES}" -ge 20 ]] && [[ "${RAM_GB}" -ge 64 ]] ); then
    IS_ULTRA=true
    ok "Mac Studio Ultra detected — applying maximum optimization"
else
    warn "Not an M3/M4 Ultra (CPU=${CPU_BRAND}, cores=${CPU_CORES}, RAM=${RAM_GB}GB)"
    warn "Applying conservative settings — some tuning may be skipped"
fi

# ── Benchmark baseline ────────────────────────────────────────────────────────
header "Baseline Benchmark"

BENCH_START=$(date +%s)
BASELINE_TOKENS_PER_SEC="unknown"

if command -v ollama &>/dev/null; then
    info "Running Ollama baseline inference test (20 tokens)…"
    BEFORE_NS=$(date +%s%N)
    RESP=$(ollama run qwen2.5-coder:14b "Say 'benchmark ok'" --nowordwrap 2>/dev/null | head -1 || echo "timeout")
    AFTER_NS=$(date +%s%N)
    ELAPSED_MS=$(( (AFTER_NS - BEFORE_NS) / 1000000 ))
    BASELINE_TOKENS_PER_SEC="~$(( 20 * 1000 / (ELAPSED_MS + 1) )) tok/s (${ELAPSED_MS}ms)"
    ok "Baseline: ${BASELINE_TOKENS_PER_SEC}"
else
    warn "Ollama not found — skipping inference benchmark"
fi

# ── Ollama configuration ──────────────────────────────────────────────────────
header "Ollama Tuning"

OLLAMA_ENV_FILE="${HOME}/.config/ollama/ollama.env"
mkdir -p "$(dirname "${OLLAMA_ENV_FILE}")"

if [[ "${IS_ULTRA}" == true ]]; then
    # 60-core GPU: use all cores; 96GB RAM: load 3 models simultaneously
    cat > "${OLLAMA_ENV_FILE}" <<EOF
# Generated by optimize_for_ultra.sh — $(date)
OLLAMA_NUM_PARALLEL=5
OLLAMA_MAX_LOADED_MODELS=3
OLLAMA_FLASH_ATTENTION=1
OLLAMA_KV_CACHE_TYPE=q8_0
EOF
    ok "Wrote Ollama env: 5 parallel, 3 models, flash attention, q8_0 KV cache"
else
    cat > "${OLLAMA_ENV_FILE}" <<EOF
# Generated by optimize_for_ultra.sh — $(date)
OLLAMA_NUM_PARALLEL=2
OLLAMA_MAX_LOADED_MODELS=2
OLLAMA_FLASH_ATTENTION=1
EOF
    ok "Wrote Ollama env: 2 parallel, 2 models, flash attention"
fi

# Extended context Modelfile for 128k context window
MODELFILE_128K="${REPO_ROOT}/config/Modelfile.qwen14b_128k"
mkdir -p "${REPO_ROOT}/config"
cat > "${MODELFILE_128K}" <<EOF
FROM qwen2.5-coder:14b
PARAMETER num_ctx $([ "${IS_ULTRA}" == true ] && echo 131072 || echo 32768)
PARAMETER num_gpu $([ "${IS_ULTRA}" == true ] && echo 99 || echo 40)
PARAMETER num_batch $([ "${IS_ULTRA}" == true ] && echo 1024 || echo 512)
PARAMETER rope_frequency_scale 1.0
SYSTEM "You are an expert software engineering assistant. Think step by step."
EOF
ok "Wrote ${MODELFILE_128K}"

if command -v ollama &>/dev/null; then
    info "Creating qwen2.5-coder:14b-128k model variant…"
    ollama create qwen2.5-coder:14b-128k -f "${MODELFILE_128K}" 2>&1 | tail -3 && ok "Model variant created" || warn "Model creation failed (Ollama may need restart)"

    if [[ "${IS_ULTRA}" == true ]]; then
        info "Testing qwen2.5-coder:32b availability (Ultra has RAM for it)…"
        if ollama show qwen2.5-coder:32b &>/dev/null; then
            ok "qwen2.5-coder:32b already available"
        else
            info "Pulling qwen2.5-coder:32b (this will take a while)…"
            ollama pull qwen2.5-coder:32b && ok "qwen2.5-coder:32b pulled" || warn "Pull failed — try manually later"
        fi
    fi
fi

# ── Multi-executor configuration ─────────────────────────────────────────────
header "Multi-Executor Setup"

WORKERS="${IS_ULTRA:+5}"
WORKERS="${WORKERS:-2}"

EXECUTOR_CONFIG="${REPO_ROOT}/config/executor_ultra.json"
cat > "${EXECUTOR_CONFIG}" <<EOF
{
  "_comment": "Ultra-optimized executor config — $(date)",
  "parallel_workers": ${WORKERS},
  "task_timeout_minutes": 15,
  "categories_by_worker": {
    "0": ["UI", "API", "CORE"],
    "1": ["MEDIA", "DATA", "ML"],
    "2": ["PERIPH", "INFRA", "SEC"],
    "3": ["LEARN", "PERF", "OBS"],
    "4": ["DOC", "TEST", "MISC"]
  },
  "model_routing": {
    "S": "qwen2.5-coder:14b-128k",
    "M": "qwen2.5-coder:14b-128k",
    "L": "qwen2.5-coder:32b",
    "XL": "qwen2.5-coder:32b"
  },
  "shared_context_cache": true,
  "kill_straggler_after_minutes": 15
}
EOF
ok "Wrote ${EXECUTOR_CONFIG} (${WORKERS} workers)"

# ── Training optimization ─────────────────────────────────────────────────────
header "Training Optimization"

TRAINING_CONFIG="${REPO_ROOT}/config/training_ultra.json"
if [[ "${IS_ULTRA}" == true ]]; then
    cat > "${TRAINING_CONFIG}" <<EOF
{
  "_comment": "Ultra training config — $(date)",
  "batch_size": 32,
  "gradient_accumulation_steps": 1,
  "max_seq_length": 8192,
  "lora_r": 64,
  "lora_alpha": 128,
  "num_train_epochs": 3,
  "learning_rate": 2e-4,
  "fp16": false,
  "bf16": true,
  "optim": "adamw_torch",
  "dataloader_num_workers": 8
}
EOF
    ok "Ultra training config: batch=32, BF16, 8 workers, LoRA r=64"
else
    cat > "${TRAINING_CONFIG}" <<EOF
{
  "_comment": "Standard training config — $(date)",
  "batch_size": 4,
  "gradient_accumulation_steps": 8,
  "max_seq_length": 4096,
  "lora_r": 16,
  "lora_alpha": 32,
  "num_train_epochs": 3,
  "learning_rate": 2e-4,
  "fp16": false,
  "bf16": false,
  "optim": "adamw_torch",
  "dataloader_num_workers": 4
}
EOF
    ok "Standard training config: batch=4 (gradient accum=8), LoRA r=16"
fi

# ── macOS performance tuning ──────────────────────────────────────────────────
header "macOS Performance"

if [[ "${IS_ULTRA}" == true ]]; then
    # Disable App Nap for Python processes
    defaults write -g NSAppSleepDisabled -bool YES 2>/dev/null && ok "Disabled App Nap" || warn "Could not disable App Nap"
    # Set high priority for Python processes (requires sudo)
    if sudo -n true 2>/dev/null; then
        sudo sysctl -w kern.timer.coalescing_enabled=0 2>/dev/null && ok "Disabled timer coalescing" || true
    fi
fi

# ── After benchmark ───────────────────────────────────────────────────────────
header "Post-Optimization Benchmark"

AFTER_TOKENS_PER_SEC="(restart Ollama to apply new settings)"
if command -v ollama &>/dev/null && [[ "${BASELINE_TOKENS_PER_SEC}" != "unknown" ]]; then
    info "Restart Ollama to apply env changes, then re-run this script with --bench-only"
fi

# ── Generate report ───────────────────────────────────────────────────────────
header "Generating Report"

mkdir -p "$(dirname "${REPORT_FILE}")"
cat > "${REPORT_FILE}" <<MDEOF
# Mac Studio Optimization Report

Generated: $(date)

## Hardware

| | Value |
|---|---|
| CPU | ${CPU_BRAND} |
| Physical cores | ${CPU_CORES} |
| RAM | ${RAM_GB} GB |
| GPU cores | ${GPU_CORES:-unknown} |
| Is Ultra | ${IS_ULTRA} |

## Baseline Performance

- Ollama inference: ${BASELINE_TOKENS_PER_SEC}
- After restart: restart Ollama and re-run \`--bench-only\` to measure post-tuning speed

## Changes Applied

### Ollama (~/.config/ollama/ollama.env)
\`\`\`
$(cat "${OLLAMA_ENV_FILE}")
\`\`\`

### Extended Context Model (config/Modelfile.qwen14b_128k)
- Context window: $([ "${IS_ULTRA}" == true ] && echo "128k tokens" || echo "32k tokens")
- GPU cores dedicated: $([ "${IS_ULTRA}" == true ] && echo "99 (all)" || echo "40")
- Batch size: $([ "${IS_ULTRA}" == true ] && echo "1024" || echo "512")

### Multi-Executor (config/executor_ultra.json)
- Parallel workers: ${WORKERS}
- Task timeout: 15 minutes
- Kill stragglers: after 15 minutes
- Expected speedup: ~${WORKERS}x ($(( 12 / WORKERS ))–$(( 15 / WORKERS )) min per item)

### Training (config/training_ultra.json)
$([ "${IS_ULTRA}" == true ] && echo "- Batch size: 32 (8x vs standard)" || echo "- Batch size: 4")
$([ "${IS_ULTRA}" == true ] && echo "- BF16 precision" || echo "- Standard FP32")
$([ "${IS_ULTRA}" == true ] && echo "- LoRA r=64 (4x larger adapters)" || echo "- LoRA r=16")

## Next Steps

1. Restart Ollama: \`brew services restart ollama\`
2. Verify model: \`ollama run qwen2.5-coder:14b-128k "hello"\`
3. Run parallel executor: \`python3 bin/auto_execute_roadmap.py --max-items 50 --workers ${WORKERS}\`
4. Monitor: \`python3 web/dashboard/server.py\` → http://localhost:8080
MDEOF

ok "Report written to ${REPORT_FILE}"

header "Summary"
echo ""
echo "  Files written:"
echo "    ${OLLAMA_ENV_FILE}"
echo "    ${MODELFILE_128K}"
echo "    ${EXECUTOR_CONFIG}"
echo "    ${TRAINING_CONFIG}"
echo "    ${REPORT_FILE}"
echo ""
echo "  Next: brew services restart ollama"
echo ""
[[ "${IS_ULTRA}" == true ]] && ok "Ultra optimizations complete — expected ~${WORKERS}x executor speedup" \
                             || ok "Standard optimizations complete"
