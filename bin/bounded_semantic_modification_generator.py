#!/usr/bin/env python3
"""Bounded semantic modification generator using local Ollama models.

Primary path: Call local Ollama for semantic code generation
Fallback path: Use deterministic patterns if semantic generation fails validation

Attribution tracking:
- local_semantic_primary: Generated by Ollama, passed validation
- local_semantic_retry: Ollama generation needed retry/cleaning
- deterministic_fallback: Fell back to pattern-based generation
- mixed: Semantic generation for part, deterministic for rest
"""

from __future__ import annotations

import json
import re
import sys
from dataclasses import dataclass, asdict
from pathlib import Path
from typing import Optional, Dict, Any
import subprocess

REPO_ROOT = Path(__file__).resolve().parents[1]

# Ollama API endpoint
OLLAMA_API = "http://localhost:11434/api/generate"

# Model selection
SEMANTIC_MODEL = "qwen2.5-coder:14b"  # Balanced: good quality, reasonable speed
FALLBACK_MODEL = "qwen2.5-coder:7b"   # Faster for retries


@dataclass
class SemanticModificationSpec:
    """Semantic modification with attribution."""
    task_id: str
    literal_old: str
    literal_new: str
    modification_type: str
    description: str
    confidence: float
    attribution: str  # local_semantic_primary, local_semantic_retry, deterministic_fallback, mixed
    model_used: Optional[str] = None
    generation_tokens: int = 0
    validation_passed: bool = False
    fallback_reason: str = ""


class BoundedSemanticModificationGenerator:
    """Semantic modification generation with fallback to deterministic patterns."""

    def __init__(self, repo_root: Path = REPO_ROOT):
        self.repo_root = repo_root
        # Import the deterministic generator as fallback
        sys.path.insert(0, str(repo_root / "bin"))
        try:
            from bounded_modification_generator import BoundedModificationGenerator
            self.deterministic_gen = BoundedModificationGenerator(repo_root)
        except ImportError:
            self.deterministic_gen = None

    def _read_file(self, rel_path: str) -> str:
        """Read file content safely."""
        path = self.repo_root / rel_path
        try:
            return path.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            return ""

    def _call_ollama(
        self,
        prompt: str,
        model: str = SEMANTIC_MODEL,
        max_tokens: int = 500,
        temperature: float = 0.1
    ) -> Optional[str]:
        """Call local Ollama model for generation."""
        try:
            payload = {
                "model": model,
                "prompt": prompt,
                "stream": False,
                "temperature": temperature,  # Configurable temperature
            }

            response = subprocess.run(
                ["curl", "-s", "-X", "POST", OLLAMA_API, "-d", json.dumps(payload)],
                capture_output=True,
                text=True,
                timeout=30
            )

            if response.returncode != 0:
                return None

            result = json.loads(response.stdout)
            return result.get("response", "").strip()
        except Exception as e:
            return None

    def _has_docstring_conflict(self, content: str, literal_new: str) -> bool:
        """Check if modification would create duplicate/conflicting docstrings."""
        # If literal_new contains a docstring, it's a conflict if:
        has_new_docstring = '"""' in literal_new or "'''" in literal_new
        if not has_new_docstring:
            return False

        # 1. If literal_new already appears in content (exact duplicate)
        if literal_new in content:
            return True

        # 2. If literal_new contains multiple docstrings (suspicious - class + method both documented)
        docstring_count = literal_new.count('"""') + literal_new.count("'''")
        if docstring_count > 2:  # More than one docstring pair
            return True

        return False

    def _is_valid_guard_clause(self, literal_new: str) -> bool:
        """Validate that generated code is actually a guard clause, not hallucination."""
        # Guard clauses must contain if statement and action
        has_if = 'if ' in literal_new
        has_action = any(x in literal_new for x in ['return', 'raise'])

        if not (has_if and has_action):
            return False

        # Guard should be reasonably short (not multi-function)
        line_count = literal_new.count('\n') + 1
        if line_count > 9:  # More than 9 lines is probably wrong
            return False

        # Reject if it's defining NEW functions/classes (not just including def/class in replacement)
        # But allow if the def/class line is FIRST (part of the anchor)
        import_pattern = any(pattern in literal_new for pattern in ['import ', 'from '])
        if import_pattern:
            return False

        # If literal_new has 'def ' or 'class ', it should only be at the very start (1 line)
        lines = literal_new.split('\n')
        if len(lines) > 1:
            # Multiple lines - first line can have def/class, but not others
            for i, line in enumerate(lines[1:], 1):
                if any(pattern in line for pattern in ['def ', 'class ', '@dataclass']):
                    return False

        return True

    def _is_valid_logging_statement(self, literal_new: str) -> bool:
        """Validate that generated code is actually a logging statement, not hallucination."""
        # Logging statements should NOT be function/class definitions, imports, logging.basicConfig, etc.
        if any(pattern in literal_new for pattern in ['def ', 'class ', 'basicConfig', 'import ', 'from ', '@dataclass']):
            return False

        # Logging statements must contain logging.* or logger.* call
        has_logging = 'logging.' in literal_new or 'logger.' in literal_new
        if not has_logging:
            return False

        # Logging should be short (single statement, 1-3 lines max)
        line_count = literal_new.count('\n') + 1
        if line_count > 4:  # More than 4 lines is probably wrong (including indentation)
            return False

        return True

    def _is_valid_print_debug(self, literal_new: str) -> bool:
        """Validate that generated code is actually a print statement, not hallucination."""
        # Print debug statements should NOT be function/class definitions, imports, etc.
        if any(pattern in literal_new for pattern in ['def ', 'class ', 'import ', 'from ', '@dataclass']):
            return False

        # Print debug must contain print( call
        has_print = 'print(' in literal_new
        if not has_print:
            return False

        # Print should be short (single statement, 1-2 lines max)
        line_count = literal_new.count('\n') + 1
        if line_count > 3:  # More than 3 lines is probably wrong
            return False

        # Should not be overly complex (no nested function calls, etc.)
        if literal_new.count('(') > 2:  # More than 2 open parens suggests nested complexity
            return False

        return True

    def _is_valid_early_return(self, literal_new: str) -> bool:
        """Validate that generated code is an early return pattern, not hallucination."""
        # Early return should NOT redefine entire function or class
        if literal_new.count('def ') > 1 or 'class ' in literal_new:
            return False

        # Early return MUST contain either 'return ' or 'raise '
        has_return = 'return ' in literal_new or 'raise ' in literal_new
        if not has_return:
            return False

        # Should contain if statement for the condition
        has_condition = 'if ' in literal_new
        if not has_condition:
            return False

        # Should be relatively short (3-5 lines for if+return block)
        line_count = literal_new.count('\n') + 1
        if line_count > 6:  # More than 6 lines is too long
            return False

        return True

    def _is_valid_assertion(self, literal_new: str) -> bool:
        """Validate that generated code is actually an assertion statement, not hallucination."""
        # Assertion statements should NOT be function/class definitions, imports, etc.
        if any(pattern in literal_new for pattern in ['def ', 'class ', 'import ', 'from ', '@dataclass']):
            return False

        # Assertion must contain 'assert ' keyword
        has_assert = 'assert ' in literal_new
        if not has_assert:
            return False

        # Assertion should have a message string (assert <condition>, "message")
        has_message = ',' in literal_new  # Most assertions have comma + message
        # Allow single assertions without message (rare but valid: assert not None)
        if not has_message and 'assert' in literal_new:
            # Check if it at least has a substantive condition
            parts = literal_new.split('assert')
            if len(parts) < 2:
                return False

        # Assertion should be short (single statement, 1-2 lines max)
        line_count = literal_new.count('\n') + 1
        if line_count > 3:  # More than 3 lines is probably wrong
            return False

        return True

    def _extract_anchor_candidates(self, content: str, description: str) -> list[str]:
        """Extract likely anchor patterns from description and file content."""
        # Look for patterns like "class X", "def X" mentioned in description
        candidates = []

        # Extract CamelCase/snake_case identifiers from description
        import re as regex_module
        identifiers = regex_module.findall(r'\b([A-Z][a-zA-Z0-9]*|[a-z_][a-z0-9_]*)\b', description)

        for ident in set(identifiers):
            # Look for exact pattern matches in file
            for pattern in [f"class {ident}:", f"def {ident}", f"def {ident}(", f"{ident}("]:
                if pattern in content:
                    candidates.append(pattern)

        return candidates

