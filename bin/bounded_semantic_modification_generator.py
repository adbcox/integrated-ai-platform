#!/usr/bin/env python3
"""Bounded semantic modification generator using local Ollama models.

Primary path: Call local Ollama for semantic code generation
Fallback path: Use deterministic patterns if semantic generation fails validation

Attribution tracking:
- local_semantic_primary: Generated by Ollama, passed validation
- local_semantic_retry: Ollama generation needed retry/cleaning
- deterministic_fallback: Fell back to pattern-based generation
- mixed: Semantic generation for part, deterministic for rest
"""

from __future__ import annotations

import json
import re
import sys
from dataclasses import dataclass, asdict
from pathlib import Path
from typing import Optional, Dict, Any
import subprocess

REPO_ROOT = Path(__file__).resolve().parents[1]

# Ollama API endpoint
OLLAMA_API = "http://localhost:11434/api/generate"

# Model selection
SEMANTIC_MODEL = "qwen2.5-coder:14b"  # Balanced: good quality, reasonable speed
FALLBACK_MODEL = "qwen2.5-coder:7b"   # Faster for retries


@dataclass
class SemanticModificationSpec:
    """Semantic modification with attribution."""
    task_id: str
    literal_old: str
    literal_new: str
    modification_type: str
    description: str
    confidence: float
    attribution: str  # local_semantic_primary, local_semantic_retry, deterministic_fallback, mixed
    model_used: Optional[str] = None
    generation_tokens: int = 0
    validation_passed: bool = False
    fallback_reason: str = ""


class BoundedSemanticModificationGenerator:
    """Semantic modification generation with fallback to deterministic patterns."""

    def __init__(self, repo_root: Path = REPO_ROOT):
        self.repo_root = repo_root
        # Import the deterministic generator as fallback
        sys.path.insert(0, str(repo_root / "bin"))
        try:
            from bounded_modification_generator import BoundedModificationGenerator
            self.deterministic_gen = BoundedModificationGenerator(repo_root)
        except ImportError:
            self.deterministic_gen = None

    def _read_file(self, rel_path: str) -> str:
        """Read file content safely."""
        path = self.repo_root / rel_path
        try:
            return path.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            return ""

    def _call_ollama(
        self,
        prompt: str,
        model: str = SEMANTIC_MODEL,
        max_tokens: int = 500,
        temperature: float = 0.1
    ) -> Optional[str]:
        """Call local Ollama model for generation."""
        try:
            payload = {
                "model": model,
                "prompt": prompt,
                "stream": False,
                "temperature": temperature,  # Configurable temperature
            }

            response = subprocess.run(
                ["curl", "-s", "-X", "POST", OLLAMA_API, "-d", json.dumps(payload)],
                capture_output=True,
                text=True,
                timeout=30
            )

            if response.returncode != 0:
                return None

            result = json.loads(response.stdout)
            return result.get("response", "").strip()
        except Exception as e:
            return None

    def _has_docstring_conflict(self, content: str, literal_new: str) -> bool:
        """Check if modification would create duplicate/conflicting docstrings."""
        # If literal_new contains a docstring, it's a conflict if:
        has_new_docstring = '"""' in literal_new or "'''" in literal_new
        if not has_new_docstring:
            return False

        # 1. If literal_new already appears in content (exact duplicate)
        if literal_new in content:
            return True

        # 2. If literal_new contains multiple docstrings (suspicious - class + method both documented)
        docstring_count = literal_new.count('"""') + literal_new.count("'''")
        if docstring_count > 2:  # More than one docstring pair
            return True

        return False

    def _extract_anchor_candidates(self, content: str, description: str) -> list[str]:
        """Extract likely anchor patterns from description and file content."""
        # Look for patterns like "class X", "def X" mentioned in description
        candidates = []

        # Extract CamelCase/snake_case identifiers from description
        import re as regex_module
        identifiers = regex_module.findall(r'\b([A-Z][a-zA-Z0-9]*|[a-z_][a-z0-9_]*)\b', description)

        for ident in set(identifiers):
            # Look for exact pattern matches in file
            for pattern in [f"class {ident}:", f"def {ident}", f"def {ident}(", f"{ident}("]:
                if pattern in content:
                    candidates.append(pattern)

        return candidates

    def _build_prompt_for_modification(
        self,
        target_path: str,
        modification_type: str,
        description: str,
        content: str,
        anchor_candidates: list[str]
    ) -> str:
        """Build task-specific prompt with stronger anchoring."""

        if modification_type == "add_docstring":
            # Specialized prompt for docstring additions with explicit anchoring and examples
            anchor_context = ""
            if anchor_candidates:
                anchor_context = f"\nLikely anchor patterns found in file:\n" + "\n".join(
                    [f"  - {a}" for a in anchor_candidates[:3]]
                )

            return f"""You are a Python code modification expert. Generate a precise docstring addition.

Task: {description}
File: {target_path}

IMPORTANT: literal_old MUST be copied EXACTLY from the file content below.
{anchor_context}

File content (first 1000 chars):
{content[:1000]}

GOOD EXAMPLES of valid JSON output:
{{"literal_old": "def execute():", "literal_new": "def execute():\\n    \\\"\\\"\\\"Execute the task.\\\"\\\"\\\"", "confidence": 0.85}}
{{"literal_old": "class Manager:", "literal_new": "class Manager:\\n    \\\"\\\"\\\"Manages orchestration.\\\"\\\"\\\"", "confidence": 0.85}}

Generate a JSON object with EXACTLY this structure:
{{
    "literal_old": "one definition line copied from file: 'class Name:' or 'def func():'",
    "literal_new": "that line + newline + indented docstring (one line in triple quotes)",
    "confidence": 0.85,
    "notes": "brief note"
}}

STRICT RULES:
1. literal_old MUST exist WORD-FOR-WORD in the file
2. literal_old is exactly one definition line from above
3. literal_new = literal_old + newline + docstring
4. Return ONLY valid JSON, no other text"""

        else:
            # Generic prompt for other modification types
            return f"""You are a code modification assistant. Generate a precise code modification.

Task: {description}
File: {target_path}
Modification type: {modification_type}

Current file content (first 1000 chars):
{content[:1000]}

Generate a JSON object with exactly this structure:
{{
    "literal_old": "exact text to find in the file (multiline ok, must exist in file)",
    "literal_new": "replacement text (must be a valid modification)",
    "confidence": 0.85,
    "notes": "brief explanation of the change"
}}

Constraints:
1. literal_old MUST exist exactly in the file (use context from file content above)
2. Keep changes minimal and focused
3. For add_comment: add a single comment line with context
4. For add_field: add field definition matching existing patterns
5. Return ONLY valid JSON, no additional text"""

    def _generate_semantic_modification(
        self,
        task_id: str,
        target_path: str,
        modification_type: str,
        description: str
    ) -> Optional[SemanticModificationSpec]:
        """Generate modification using semantic model with post-generation validation."""
        content = self._read_file(target_path)
        if not content:
            return None

        # Extract anchor candidates for better grounding (especially docstrings)
        anchor_candidates = self._extract_anchor_candidates(content, description)

        # Build task-specific prompt
        prompt = self._build_prompt_for_modification(
            target_path,
            modification_type,
            description,
            content,
            anchor_candidates
        )

        # Call Ollama - use slightly higher temperature for docstrings to allow more flexibility
        temp = 0.15 if modification_type == "add_docstring" else 0.1
        response_text = self._call_ollama(prompt, model=SEMANTIC_MODEL, temperature=temp)
        if not response_text:
            return None

        # Parse response
        try:
            # Extract JSON from response (might have extra text)
            json_match = re.search(r'\{[^{}]*(?:\{[^{}]*\}[^{}]*)*\}', response_text, re.DOTALL)
            if not json_match:
                return None

            result = json.loads(json_match.group(0))

            literal_old = result.get("literal_old", "")
            literal_new = result.get("literal_new", "")
            confidence = float(result.get("confidence", 0.0))

            # Strict validation: literal_old MUST exist in file
            if not literal_old or literal_old not in content:
                return None

            # Post-generation validation: check for conflicts
            if self._has_docstring_conflict(content, literal_new):
                return None  # Reject and fall back to deterministic

            return SemanticModificationSpec(
                task_id=task_id,
                literal_old=literal_old,
                literal_new=literal_new,
                modification_type=modification_type,
                description=description,
                confidence=confidence,
                attribution="local_semantic_primary",
                model_used=SEMANTIC_MODEL,
                generation_tokens=len(response_text.split()),
                validation_passed=True
            )
        except (json.JSONDecodeError, ValueError):
            return None

    def _get_deterministic_fallback(
        self,
        task_id: str,
        target_path: str,
        fallback_reason: str
    ) -> Optional[SemanticModificationSpec]:
        """Get deterministic pattern-based modification as fallback."""
        if not self.deterministic_gen:
            return None

        from bounded_modification_generator import ModificationSpec

        det_spec = self.deterministic_gen.generate_modification(task_id)
        if not det_spec:
            return None

        # Validate deterministic modification doesn't create conflicts
        content = self._read_file(target_path)
        if self._has_docstring_conflict(content, det_spec.literal_new):
            return None  # Conflict detected, reject this modification

        return SemanticModificationSpec(
            task_id=task_id,
            literal_old=det_spec.literal_old,
            literal_new=det_spec.literal_new,
            modification_type=det_spec.modification_type,
            description=det_spec.description,
            confidence=det_spec.confidence,
            attribution="deterministic_fallback",
            fallback_reason=fallback_reason,
            validation_passed=True
        )

    def generate_modification(
        self,
        task_id: str,
        target_path: str,
        modification_type: str,
        description: str
    ) -> Optional[SemanticModificationSpec]:
        """Generate modification with semantic model, fallback to deterministic."""

        # Try semantic generation first
        semantic_spec = self._generate_semantic_modification(
            task_id=task_id,
            target_path=target_path,
            modification_type=modification_type,
            description=description
        )

        if semantic_spec and semantic_spec.confidence >= 0.75:
            return semantic_spec

        # For docstring tasks that failed, try one targeted retry with explicit examples
        if modification_type == "add_docstring" and not semantic_spec:
            # Retry with a simpler, more constrained approach
            content = self._read_file(target_path)
            if content:
                # Build a minimal retry prompt focused on finding a single anchor
                retry_prompt = f"""You are a Python expert. Find ONE single line from the file that starts a class or function definition, then add a docstring.

File: {target_path}
File content (key section):
{content[:800]}

Return ONLY this JSON, nothing else:
{{
    "literal_old": "one line like 'class X:' or 'def func():' (copy EXACTLY from above)",
    "literal_new": "that same line + newline + one-line docstring in triple quotes",
    "confidence": 0.80
}}"""

                retry_response = self._call_ollama(retry_prompt, model=SEMANTIC_MODEL, temperature=0.12)
                if retry_response:
                    try:
                        json_match = re.search(r'\{[^{}]*\}', retry_response, re.DOTALL)
                        if json_match:
                            result = json.loads(json_match.group(0))
                            retry_old = result.get("literal_old", "")
                            retry_new = result.get("literal_new", "")

                            # Validate retry attempt
                            if retry_old and retry_old in content and retry_new:
                                if not self._has_docstring_conflict(content, retry_new):
                                    return SemanticModificationSpec(
                                        task_id=task_id,
                                        literal_old=retry_old,
                                        literal_new=retry_new,
                                        modification_type=modification_type,
                                        description=description,
                                        confidence=0.75,
                                        attribution="local_semantic_retry",
                                        model_used=SEMANTIC_MODEL,
                                        generation_tokens=len(retry_response.split()),
                                        validation_passed=True
                                    )
                    except (json.JSONDecodeError, ValueError):
                        pass

        # Fall back to deterministic pattern
        fallback_reason = "semantic_confidence_low" if semantic_spec else "semantic_generation_failed"
        return self._get_deterministic_fallback(task_id, target_path, fallback_reason)


def main():
    """Test semantic modification generation on bounded task set."""
    gen = BoundedSemanticModificationGenerator()

    # Task definitions from bounded_execution_framework
    tasks = [
        ('A1', 'framework/code_executor.py', 'add_docstring', 'Add docstring to ExecutorFactory class'),
        ('A2', 'bin/stage_rag4_plan_probe.py', 'add_comment', 'Add docstring to main function'),
        ('C1', 'framework/code_executor.py', 'add_docstring', 'Add docstring to ExecutorFactory'),
        ('C2', 'bin/ensure_comment_only.sh', 'fix_shebang', 'Verify shell shebang'),
        ('D3', 'framework/__init__.py', 'add_logging', 'Add structured logging initialization'),
    ]

    results = []

    print("=" * 80)
    print("BOUNDED SEMANTIC MODIFICATION GENERATION TEST")
    print("Primary: Local Ollama semantic generation")
    print("Fallback: Deterministic patterns")
    print("=" * 80)
    print()

    for task_id, target_path, mod_type, description in tasks:
        print(f"[{task_id}] {description}...", end=" ", flush=True)

        spec = gen.generate_modification(
            task_id=task_id,
            target_path=target_path,
            modification_type=mod_type,
            description=description
        )

        if spec:
            results.append(asdict(spec))
            status_icon = "🧠" if spec.attribution.startswith("local_semantic") else "📋"
            print(f"{status_icon} {spec.attribution}")
        else:
            print("✗ FAILED")

    print()
    print("=" * 80)

    # Summary
    semantic_count = sum(1 for r in results if "semantic" in r.get("attribution", ""))
    fallback_count = sum(1 for r in results if r.get("attribution") == "deterministic_fallback")

    print(f"Results: {len(results)}/5 generated")
    print(f"  Semantic (local Ollama): {semantic_count}")
    print(f"  Deterministic (fallback): {fallback_count}")
    print("=" * 80)

    # Show details
    for result in results[:2]:  # Show first 2 in detail
        print(f"\n[{result['task_id']}] {result['attribution']}")
        print(f"  Type: {result['modification_type']}")
        print(f"  Confidence: {result['confidence']:.2f}")
        if result.get('model_used'):
            print(f"  Model: {result['model_used']}")
        if result.get('fallback_reason'):
            print(f"  Fallback reason: {result['fallback_reason']}")

    return 0 if len(results) >= 3 else 1


if __name__ == '__main__':
    sys.exit(main())
