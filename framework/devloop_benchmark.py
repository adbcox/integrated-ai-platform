class DevloopBenchmarkRunner:
    def __init__(self, artifact_root: Path):
        self._artifact_root = artifact_root

    def run(
        self,
        tasks: tuple,
        *,
        task_tmpdir: Path,
        session_like: Any,
        workspace_like: Any,
        gate: Any,
        scope: ToolPathScope,
        runner: Any = None,
    ) -> DevloopBenchmarkResult:
        passed = 0
        failed = 0
        task_results: List[dict] = []

        for task in tasks:
            target_path = task_tmpdir / task.file_name
            target_path.write_text(task.initial_content, encoding="utf-8")

            devloop = FileLocalDevloopRunner(
                session_like=session_like,
                workspace_like=workspace_like,
                gate=gate,
                scope=scope,
                runner=runner,
            )
            fl_task = FileLocalTask(
                session_id=_extract_session_id(session_like),
                target_path=str(target_path),
                old_string=task.old_string,
                new_string=task.new_string,
                task_kind=task.task_kind,
            )
            result: FileLocalResult = devloop.run_task(fl_task)

            content_correct = target_path.read_text(encoding="utf-8") == task.expected_content if result.patch_applied else False
            task_passed = result.patch_applied and content_correct
            if task_passed:
                passed += 1
            else:
                failed += 1

            task_results.append({
                "task_id": task.task_id,
                "task_kind": task.task_kind,
                "passed": task_passed,
                "patch_applied": result.patch_applied,
                "error": result.error,
            })

        outcome = "pass" if failed == 0 else "fail"

        self._artifact_root.mkdir(parents=True, exist_ok=True)
        artifact_path = self._artifact_root / "devloop_benchmark_result.json"
        summary = {
            "total_tasks": len(tasks),
            "passed": passed,
            "failed": failed,
            "outcome": outcome,
            "task_results": task_results,
        }
        artifact_path.write_text(json.dumps(summary, indent=2), encoding="utf-8")

        return DevloopBenchmarkResult(
            total_tasks=len(tasks),
            passed=passed,
            failed=failed,
            outcome=outcome,
            task_results=task_results,
            artifact_path=str(artifact_path),
        )


def _extract_session_id(session_like: Any) -> str:
    if isinstance(session_like, dict):
        return str(session_like.get("session_id", "benchmark"))
    if hasattr(session_like, "session_id"):
        return str(session_like.session_id)
    return "benchmark"


