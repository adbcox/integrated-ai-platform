class MVPBenchmarkRunner:
    def __init__(self, artifact_root: Path):
        self._artifact_root = artifact_root

    def run(
        self,
        tasks: tuple,
        *,
        task_tmpdir: Path,
        session_like: Any,
        workspace_like: Any,
        gate: Any,
        scope: ToolPathScope,
        runner: Any = None,
    ) -> MVPBenchmarkResult:
        passed = 0
        failed = 0
        task_results: List[dict] = []

        for spec in tasks:
            target_path = task_tmpdir / spec.file_name
            target_path.write_text(spec.initial_content, encoding="utf-8")

            loop_runner = MVPCodingLoopRunner(
                session_like=session_like,
                workspace_like=workspace_like,
                gate=gate,
                scope=scope,
                runner=runner,
            )
            mvp_task = MVPTask(
                session_id=_get_session_id(session_like),
                target_path=str(target_path),
                old_string=spec.old_string,
                new_string=spec.new_string,
                task_kind=spec.task_kind,
                enable_revert=False,
            )
            loop_result: MVPLoopResult = loop_runner.run_task(mvp_task)

            content_correct = (
                target_path.read_text(encoding="utf-8") == spec.expected_content
                if loop_result.patch_applied else False
            )
            task_passed = loop_result.patch_applied and content_correct

            if task_passed:
                passed += 1
            else:
                failed += 1

            task_results.append({
                "task_id": spec.task_id,
                "task_kind": spec.task_kind,
                "passed": task_passed,
                "patch_applied": loop_result.patch_applied,
                "error": loop_result.error,
            })

        outcome = "pass" if failed == 0 else "fail"

        self._artifact_root.mkdir(parents=True, exist_ok=True)
        artifact_path = self._artifact_root / "mvp_benchmark_result.json"
        summary = {
            "total_tasks": len(tasks),
            "passed": passed,
            "failed": failed,
            "outcome": outcome,
            "task_results": task_results,
        }
        artifact_path.write_text(json.dumps(summary, indent=2), encoding="utf-8")

        val_path = self._emit_campaign_validation(session_like, outcome, artifact_path)

        return MVPBenchmarkResult(
            total_tasks=len(tasks),
            passed=passed,
            failed=failed,
            outcome=outcome,
            task_results=task_results,
            artifact_path=str(artifact_path),
            validation_artifact_path=val_path,
        )

    def _emit_campaign_validation(self, session_like: Any, outcome: str, artifact_path: Path) -> Optional[str]:
        try:
            sid = _get_session_id(session_like)
            return emit_loop_validation(
                emitter="mvp_benchmark",
                session_id=sid,
                job_id="mvp-bench-campaign",
                outcome=outcome,
                step_results=({"step": "benchmark", "success": outcome == "pass"},),
                artifact_dir=self._artifact_root,
            )
        except Exception:
            return None


def _get_session_id(session_like: Any) -> str:
    if isinstance(session_like, dict):
        return str(session_like.get("session_id", "benchmark"))
    if hasattr(session_like, "session_id"):
        return str(session_like.session_id)
    return "benchmark"


